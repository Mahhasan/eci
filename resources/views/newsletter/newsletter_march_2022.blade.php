                        <h3>About SheTrades.com</h3>
                        <p>Women’s economic empowerment has appeared on the global development agenda for two decades, however, the 
                          business and economic cases are still not formulated fully. In 2015, consultations with global thought leaders
                          revealed persisting silos between relevant actors and the components of women’s enterprise development: 
                          gender-disaggregated research, inclusive policy, skills development, access to markets and finance. Women’s 
                          economic empowerment is not a matter for government policy, the private sector, or social change alone. All 
                          have critical roles to play. This is why ITC launched the SheTrades initiative, which seeks to connect three 
                          million women entrepreneurs to market by 2021. 
                        </p><br>

                        <h3>Market analysis tools</h3>
                        <p>Users from developing countries and territories as well as the European Union can use all features and 
                          functionality including the bulk data download option free of charge. Most of the content in the International 
                          Trade Centre’s (ITC) Market Analysis Tools is free for all users. The tools enable companies and trade support 
                          institutions to identify export and import opportunities and compare market-access requirements, and they help 
                          policymakers monitor national trade performance and prepare for trade negotiations. 
                          ITC has developed online tools – Trade Map, Market Access Map, Investment Map, Trade Competitiveness Map, Export 
                          Potential Map, Procurement Map and Sustainability Map – that collectively help improve transparency in 
                          international trade. To access the tools register at the portal, https://marketanalysis.intracen.org/en
                          <br>
                          ITC is also part of a multi-agency initiative to make trade information more accessible for MSMEs through the 
                          Global Trade Helpdesk.
                        </p>

                        <h3>Trade Map</h3>
                        <p>
                          The Trade Map provides on-line access to one of the world’s largest trade databases and presents indicators 
                          on export performance, international demand, alternative markets and the role of competitors from both the 
                          product and country perspective. Company contact information is also available for 64 countries.
                        </p>
                        
