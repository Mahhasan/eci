                        <h3>Market Access Map</h3> 
                        <p>
                          The Market Access Map provides information on applied customs tariffs, including Most Favoured Nation (MFN) 
                          tariffs and unilateral and trade agreement preferences. The application also covers tariff rate quotas, 
                          trade remedies, rules of origin, plus the corresponding certificates, bound tariffs of World Trade Organization 
                          (WTO) members, non-tariff measures and trade flows.
                        </p>
                        
                        <button class="btn" style="background-color: #002147;"><a href="https://www.macmap.org/" target="_blank" style="text-decoration: none; color: #dddddd; font-size: 14px;">
