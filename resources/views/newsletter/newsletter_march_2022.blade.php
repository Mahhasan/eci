                        <h3>Investment Map</h3> 
                        <p>
                          The Investment Map combines statistics on foreign direct investment and international trade, tariff data and the 
                          activities of multinational firms into an easy-to-use, interactive internet tool that allows analysis by country, 
                          trading partner and sector.
                        </p>
                         
