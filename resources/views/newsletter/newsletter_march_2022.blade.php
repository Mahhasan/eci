                        <h3>Trade Competitiveness Map</h3> 
                        <p>
                          Governments need to monitor trade performance in order to provide adequate trade policy support to small and 
                          medium-sized enterprises. Trade and Investment Support Institutions (TISIs) have to identify the right 
                          sectors on which to spend specific sector-development strategy. Firms need to scan global markets for product 
                          and market diversification opportunities. Trade Competitiveness Map makes it easier for all three sets of actors 
                          to conduct strategic market research, monitor national export performance and design trade development programs.
                        </p>
                        
