                        <h3>SheTrades.com</h3> 
                        <p>
                          Shetrades.com (website and mobile app) is a unique platform for women-owned businesses, organisations, 
                          companies and ITC SheTrades partner institutions:
                          <br>
                          Women entrepreneurs can use the platform to showcase their businesses, build strong networks, strike 
                          business deals, increase their credibility and connect to markets.
                          <br>
                          Companies and individual buyers can use shetrades.com to include more women entrepreneurs in their supply 
                          chains, by sourcing specific products & services from women-owned businesses.
                          <br>
                          Registered users can gain free access to SheTrades Virtual Learning, the core training hub for the SheTrades 
                          initiative, where they can participate in group activities and live webinars, and take courses developed by experts.
                          <br>
                          Users can apply for workshops, trade fairs and other business events. Within a growing number of countries, women 
                          entrepreneurs registered with SheTrades are given opportunities to attend dynamic and enriching in-country activities, 
                          all of which are made possible by ITC SheTrades partners and sponsors.
                          <br>
                          Users can stay up to date with ITC SheTrades activities, news, success stories, videos, publications and more. 
                          There is a lot of action to be witnessed, information to be shared, and conversations to be had within the SheTrades community.
                        </p>

                        <h3>SheTrades Projects</h3> 
                        <p>
                          Under the SheTrades platform, selected women-owned businesses and corporations will benefit from a wide range of 
                          opportunities to expand their business and jointly advance women economic empowerment.
                        </p>

                        <h3>Ongoing projects</h3> 
                        <h4>SheTrades and the Women's Entrepreneurship Accelerator</h4>
                        <p>
                          ITC SheTrades and the Women’s Entrepreneurship Accelerator have teamed up to inspire, educate, and empower women 
                          entrepreneurs around the world. Powered by Mary Kay, the Accelerator will offer a guided entrepreneurship 
                          curriculum supplemented by on-the-ground training for women from developing countries interested in entrepreneurship 
                          and integrating into regional and global value chains.
                          <br>
                          The Women's Entrepreneurship Accelerator is a multi-stakeholder strategic partnership between five United Nations 
                          agencies: UN Women, International Labour Organization (ILO), International Trade Centre (ITC), UN Global Compact 
                          (UNGC), and the United Nations Development Programme (UNDP).
                        </p>

                        <h4>SheTrades Rwanda</h4> 
                        <p>
                          The project is a component of the EIF funded “Project to Enhance the Effectiveness and Efficiency of Export 
                          Growth Initiatives” managed by the Ministry of Trade and Industry of Rwanda (MINICOM).
                          <br>
                          Funded by the Enhanced Integrated Framework (EIF), the SheTrades Rwanda project is implemented by ITC Women 
                          and Trade programme from September 2020 - February 2022.
                        </p>

                        <h4>SheTrades AfCFTA</h4> 
                        <p>
                          Funded by GIZ, the SheTrades: Empowering Women in the AfCFTA project aims to support women-owned businesses 
                          to benefit from trade opportunities created by the AfCFTA through capacity building, networking, and advocacy.
                          Women’s economic empowerment is crucial to unlocking the full potential of the African Continental Free 
                          Trade Area (AfCFTA) and achieving the Sustainable Development Goals.
                        </p>

                        <h4>SheTrades Outlook</h4> 
                        <p>
                          ⚖️ SheTrades Outlook helps track how laws, policies, and practices in different countries affect women’s 
                          participation in business and trade, covering 83 indicators across 6 policy areas. The first tool of its 
                          kind to make trade possible for women by promoting a more inclusive policy ecosystem. It provides 
                          comprehensive data and analysis on trade and women’s economic empowerment, and shares experiences from 
                          around the world.
                        </p>

                        <h4>SheTrades Zambia</h4> 
                        <p>
                          How are women-owned businesses benefitting?
                          <br>
                          A project that aims to increase the participation of Zambian women-owned businesses in the economy and trade 
                          by enhancing their competitiveness and strengthening their market linkages. The project is funded by the 
                          Enhanced Integrated Framework and implemented in collaboration with the Ministry of Trade, Commerce and 
                          Industry, and the Zambia Development Agency (ZDA).
                        </p>

                        <h4>SheTrades in The Gambia</h4> 
                        <p>
                          Women-owned businesses:
                          <br>
                          SheTrades in the Gambia aims to facilitate the integration of Gambian women-owned businesses in the economy 
                          and increase their participation in trade. The initiative will be implemented in collaboration with the 
                          Ministry of Trade, Regional Integration and Employment.
                        </p>

                        <h4>SheTrades and UPS</h4> 
                        <p>
                          ITC SheTrades and UPS have teamed up to help women in business succeed in international markets by making the 
                          process of exporting easier and more efficient. We’re working to provide training and education opportunities 
                          that allow business owners to better understand the complexities of exporting, e-commerce and trade regulations. 
                          The project is aligned with the UPS Women Exporters Program, designed to train thousands of women and promote the 
                          growth of women-owned SMEs. 
                        </p>

                        <h4>SheTrades in the IORA region</h4> 
                        <p>
                          The SheTrades in the IORA region was implemented by the International Trade Centre’s (ITC) SheTrades initiative 
                          in three IORA member countries, Indonesia, Kenya and Sri Lanka. The project focused on two sectors: tourism and 
                          the information technology (IT) and business process outsourcing (BPO) sectors. In Sri Lanka, the project also 
                          covered tourism-related products since the sector involves a relatively high number of Sri Lankan women.
                          <br>
                          The “Economic Empowerment of Women in the Indian Ocean Rim Association (IORA) Countries” project (SheTrades in the IORA region)
                        </p>

                        <h4>SheTrades Commonwealth</h4> 
                        <p>
                          The SheTrades in the Commonwealth project aims to facilitate the participation of women-owned businesses in 
                          international trade and increase job creation in Commonwealth countries.
                        </p>

                        <h4>SheTrades Pacific</h4> 
                        <p>
                          In Papua New Guinea, the project’s aim is to transform the largely informal, fragmented and scattered activity of 
                          bilum twisting into an economically viable business opportunity for selected groups of women producers. In Samoa, 
                          the project supports the increased participation of businesswomen in government-procurement markets. In Vanuatu, 
                          the project links women smallholders and their communities to the tourism value chain on Espiritu Santo Island, the 
                          fastest-growing cruise-ship destination in Vanuatu.
                        </p>

                        <h4>SheTrades in Agribusiness</h4> 
                        <p>
                          The aim of this project is to implement recommendations of the national development strategy for yam and 
                          associated farming systems developed jointly by ITC, the International Institute of Tropical Agriculture 
                          (IITA) and national stakeholders. The strategy began in 2012 and has been led by the Ghanaian private sector 
                          with the support of the government of Ghana.
                          <br>
                          By providing support to partner institutions and government agencies, this project links women and their associated 
                          agribusinesses to commercially viable opportunities and value chains. It improves the livelihoods of farming 
                          communities and creates job opportunities across several sectors, focusing on identifying global market 
                          opportunities for Ghana’s fresh and processed yam.
                        </p>
                      
                        <h4>SheTrades in Ethiopia and Mongolia</h4> 
                        <p>
                          Women entrepreneurs from these sectors who have participated at events linked to the Global Platform for Action 
                          on Sourcing from Women Vendors have had substantial interest from buyers. ITC is now providing training and 
                          mentoring to selected women-owned enterprises so that they can meet the requirements needed to strike deals.
                        </p>

                        <h4>SheTrades Cotton</h4> 
                        <p>
                          This project sought to empower women in the Zambian cotton sector by providing technical support to enterprises 
                          and institutions. The project supported women farmers to play a more instrumental role within the cotton value 
                          chain in areas such as ginning by strengthening the gender-related work of the Cotton Association of Zambia. Women 
                          farmers were able to enhance their skills in business development using the ACCESS! training programme developed for
                          women exporters. 
                        </p>

                        <h4>SheTrades Coffee</h4> 
                        <p>
                          ITC has also facilitated the creation of national chapters of the International Women’s Coffee Alliance 
                          (IWCA) in a number of countries, to empower women in the coffee sector. IWCA’s mentoring and management 
                          programmes have boosted productivity and incomes. Working with buyers and producers to improve the profile of 
                          coffee produced by women has resulted in more than 100 major transactions. About 200 women coffee producers are 
                          now selling to large institutional buyers throughout the world.
                        </p>

