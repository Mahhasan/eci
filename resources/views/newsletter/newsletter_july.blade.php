<h3 style="text-transform: uppercase; text-align: left;">Webinar on ‘Effect of Non-Tariff Measures in Trade between Bangladesh and India' </h3>
<div class="serif" style="color: #1f2225; font-size: 14px; font-weight: 400; line-height: 20px; margin: 0; margin-bottom: 15px; margin-top: 10px; padding: 0; text-align: justify;">
	On July 13, 2021, Enterprise Competitiveness Institute (ECI) in cooperation with International Affairs (IA) organized the Webinar on <b>‘Effect of Non-Tariff Measures in Trade between Bangladesh and India'</b> (virtual platform: Google Meet) which was aired at CampusTV Live. The aim of the event was to address the Non Tariff Measures issue in trade between Bangladesh and India and NTM is an important area of International Trade. Moreover, such a webinar will also be useful for the trade related policy makers and international agencies as well. <br>
<br>
<img src="https://i.imgur.com/viKmnBL.jpg" style="width: 500px;padding: 10px;"> 
<br>
Two international speakers: Dr. R Sugant Dean School of Management & School of Economics and Commerce, CMR University Bangalore, India and Dr. Ranjeesh Kler Associate Director (Planning and Monitoring) and Head of the Department , Department of Economics , School of Business and Economics, Adamas University , Kolkata , India have attended as one of the key speakers of the event whereas two national speakers were Mr. Md. Mamun-Ur-Rashid Askari, Deputy Chief (International Cooperation Division) of Bangladesh Trade and Tariff Commission, Ministry of Commerce, Government of the People’s Republic Bangladesh & Mr. Nafees Imtiaz Islam, Deputy Director, Institutional Quality Assurance Cell (IQAC) Daffodil International University (DIU) have stressed on different NTM issues.  <br>
<br>
Dr. R. Sugant has talked about different dimensions of NTMs and addressed various issues like challenges Bangladesh has been facing while exporting to India (e.g. high tariffs, protectionist policies, price sensitivity, infrastructure, local content requirement, regulatory changes, protection of IP), Lines of Credit (LoC), cooperation in power sector,   India Bangladesh trade,  affect trade between these two countries. Dr. Rajneesh Kler has talked about NTM issues of two countries like bilateral trade remedies, agricultural trade among Bangladesh and India, different trade routes and regulatory, technical, procedural & infrastructural barriers etc.   
<br>
</br>
From the Bangladesh part, Mr. Mamoon and Mr. Nafees have focused on issues like Agreement on application of Sanitary Phytosanitary (SPS) Mesures, Technical barriers to trade (TBT), legal framework of SPS and TBT,  harmonisation of food standards, mutual recognition of conformity assessment, dispute settlement and technical cooperation with transparency, National Single Window (NSW)  etc. <br>
<br>
After the presentations, the speakers attended a ‘Question & Answer’ session and actively interacted with the audience to share their views. <br>

<br>

While wrapping up the programme, Syed Raihan-Ul-Islam, Senior Assistant Director (International Affairs), Daffodil International University has extended thanks to the guests and participants for their kind participation. <br>
