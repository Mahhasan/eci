                      >
                        The Bangladesh, Bhutan, India, Nepal (BBIN) sub-regional initiative is envisioned
                        to improve economic cooperation and connectivity among the four South Asian countries.
                        This initiative is expected to help the landlocked developing countries of this region to 
                        integrate more effectively with the global economy. The long-term success of BBIN will depend 
                        on how well these concerns are managed within the framework.
                        Although countries of South Asia are tied by shared history and culture, they are still not 
                        well connected with each other
                        and integration remains one of the poorest in the world. The Bangladesh, Bhutan, India, Nepal 
                        (BBIN) sub regional initiative is envisioned to improve economic cooperation and connectivity
                        among these four South Asian countries.
 
						<br><br>
						<h3>Background</h3>
            <p>In light of economic interdependence demonstrated by "growth triangles" 
              across Asia and hitherto unheeded concerns of eastern subcontinent nations,
              its Council of Ministers in May 1996 approved a sub-regional body of Nepal,
              Bhutan, north east India and Bangladesh as the South Asian Growth Quadrangle (SAGQ).
             A Male summit one year later agreed to co-ordinate efforts catered "to the special
              individual needs of three or more Member States, and formalized procedures focused 
              on the subcontinent's north east to develop intra-regional trade and investment, tourism,
              communication, and energy resources. Particular areas of collaboration were targeted to channel
               growth and complementarity. </p>
                <img
                  class="mobile-padding-bottom"
                  src="https://i.imgur.com/qtPGvOE.jpg"
