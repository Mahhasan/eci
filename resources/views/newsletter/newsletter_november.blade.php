                        <p>As the mechanism operated through specific projects, constituent members endeavored to
                  integrate without requiring alteration in broader policy or methods of governance. It sought
                    to impel the subregion's latent socio-economic potential, harnessing disparate stages of development to
                    augment each other. Over years its objectives expanded to incorporate land and port connectivity. Procurement
                      of funds for this purpose was discussed at ADB headquarters, Manila, where formulation of the South Asia Sub
                      regional Economic Cooperation (SASEC) Program within auspices of ADB concluded in March 2001.
                  </p>
                            <h3>Framework</h3> 
                  Through regular Joint Working Group (JWG) sessions, representatives explore avenues 
                  of co-operation, exchange experiences and best practices, review data sharing arrangements 
                  for disaster forecasts and mitigation, besides strengthening transit facilitation measures 
                  such as shared border stations on key routes and harmonized customs procedures. The priority of 
                  "connectivity" further embodies seamless electrical grids, shared access to road, rail, air and port 
                  infrastructure, and ease of travel. To such ends, a sub-regional Motor Vehicle Agreement that garnered 
                  assent in Thimphu would allow buses and later private vehicles with BBIN permits to travel unobstructed 
                  by border hindrances.

                        <br />
                        <br />
                       <h3>BBIN - Motor Vehicle Agreement</h3>
                       Bangladesh, Bhutan, India and Nepal (BBIN) Motor Vehicles Agreement (MVA) is a step
                      towards improving the economic situations of these South-Asian countries through regional
                      cooperation. With superpowers like China on the rise and its Belt Road Initiative,
                      this agreement showcases the strength of regional understanding. Such agreements solidify
                      South-Asian relations which can also be interpreted as a defense against the influence of
                      China in the South-Asian region.
                      <br><br>
                      The BBIN economic project aims at constructing an economic corridor connecting Bangladesh, Bhutan,
                      India and Nepal by roads. A meeting of the Ministers of Transport took place on June 15, 2015,
                      in Thimpu, Bhutan. The agreement was created ‘for the Regulation of Passenger,
                      Personal and Cargo Vehicular Traffic between Bangladesh, Bhutan, India, and Nepal’ 
                      and improve the quality of transportation between these neighboring countries also
                      aiding the economy. The corridor will enhance connectivity between goods, people and 
                      support easier transportation. The meeting also put forward a six-month plan from July
                      to December 2015 to map out the intricacies of the agreement.
                      The formation of this agreement came after the 18th South Asian Association for Regional Cooperation (SAARC) summit
                      held in Kathmandu, Nepal that preached and encouraged the South-Asian countries to expand network and connectivity 
                      in the region. The initiative is also determined to keep the process environment friendly and economically efficient
                      therefore ensuring that all countries involved can reap the benefits.
                      <br><br>
                       In 2020, a meeting was held in New Delhi between the participant countries to
                        discuss the BBIN agreement and its progress. The agenda of the meeting was to
                         discuss the protocols regarding the movement of passengers and cargo and its
                          regulation. Some of the key aspects of the agreement are:
                          <ul>
                            <li>
                              Member states would be permitted to operate their vehicles in each other’s 
                              territory for transportation of cargo and passengers.
                            </li>  
                            <li>
                              To enter a neighboring country's territory, all cars would need an electronic permit,
                              and border security measures across nations' borders would remain in place.
                            </li>
                            <li>
                              Cargo vehicles will be allowed to enter any of the four countries without the
                               requirement for products to be transferred from one truck to another at the border.
                            </li>
                            <li>
                              Cargo trucks are tracked electronically, permits are granted online, and 
                              permits are delivered electronically to all land ports under the system.
                            </li>
                            <li>
                              Every time the container door is opened, an electronic seal on the vehicle informs authorities.  
                            </li>
                          </ul>
                          <p>The implementation of the agreement has also gone through multiple stages including trial 
                            runs of transport routes and vehicles. Furthermore, vehicles travelling on defined routes
                            require transportation service facilities such as relaxation centers, gasoline refueling stations, 
                            and any other amenities needed by both crew members and vehicles. In its various sections,
                             the MVA expressly addresses all of these elements.</p>
                             <p>A key drawback in multi-lateral agreements is the coordination it requires and the inability
                              for countries to meet that need. Meetings, discussions, forums, and physical implementation
                              of those are essential for these agreements to function. Another important factor of this 
                              agreement is its political factor. </p>
                              <p>All these various challenges have prevented substantial regional growth in South-Asia 
                              that can only see success when these countries take a step towards independence and include
                              regional cooperation as an important aspect in their diplomacy, initiatives and policies.</p>
                              <h3>Opportunities</h3>
                              <p>The BBIN MVA can be a game-changer for neighborhood cooperation. For the first time these 
                              countries have decided to exchange their traffic rights and provides transit to cargo and passenger
                              vehicles within and across international borders. The primary goal is to develop functional transport
                              corridors and subsequently convert them to economic corridors. These economic corridors are expected 
                              to play an instrumental role in strengthening existing value chains and creating new ones.</p>
                              
                            <p>According to the Organization for Economic Cooperation and Development (OECD),
                              a 10 per cent improvement in transport and trade-related infrastructure quality
                                can increase agricultural exports of developing countries by 30 per cent.</p>
                            <p>While infrastructure deficits for effective implementation of the BBIN MVA are being 
                              and can be addressed through investments including public-private partnerships, they
                              are not sufficient. On-the-ground political economy factors are to be addressed through 
                              innovative, customized solutions so that local stakeholders can realize how beneficial it 
                              can be for generating employment and other opportunities for their livelihood security.</p>
                       
                     
