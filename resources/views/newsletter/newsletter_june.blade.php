<h3 style="text-transform: uppercase; text-align: left;">COVID 19 and International Trade: Special References to the Land Port Management of Selected Countries of South Asia</h3>
<div class="serif" style="color: #1f2225; font-size: 14px; font-weight: 400; line-height: 20px; margin: 0; margin-bottom: 15px; margin-top: 10px; padding: 0; text-align: justify;">
	<b>Nafees Imtiaz Islam</b><br>
Deputy Director, Institutional Quality Assurance Cell (IQAC), <br>
Daffodil International University <br>
nafees-research@daffodilvarsity.edu.bd <br>
<br>
During the pandemic, nCOVID 19, we have observed the multifaceted nature of international trade as for example we can look into how the use of Non-Tariff Measures (NTMs) have been instrumental during the pandemic, as a number of temporary measures were put in place by over 140 countries for safeguarding public health over economic concerns. The pandemic affects trade among member countries of different regional blocks. Reportedly, early in March, Bangladesh, Bhutan, India and Nepal (BBIN Countries) have imposed restrictions of the people’s movement and freight among these countries, which has affected the supply chain, which dents impact on trade flows. During COVID 19 situation, thousands of loaded trucks with perishable goods were compelled to be stuck at the Bangladesh-India border which resulted in losses. Authorities also struggled to give the right solution in mitigating the risks associated with COVID 19. The World Bank estimates that South Asian countries will likely experience the worst economic performance of the past 40 years. The WTO is forecasting a year-on-year drop in global trade of around 18.5 percent. <br>
<br>
<b>COVID-19 and digital transformation in trade processes </b><br>
Digitization and automation of the process involving trade, works as a catalyst in reducing trade cost and making the trade more resilient and we have observed the transformation during COVID 19. According to the DDHL International GmbH (DHL), “those countries that have minimized bureaucracy and cost at the border have been able to benefit ahead of those that haven’t”. <br>
Urgency for such transformative change becomes compelling in countries, where Bangladesh shares international boundaries and those South Asian countries, more specifically, Bangladesh, Bhutan, India, Nepal (BBIN) member countries. In this sub-region, trade is paper-heavy, and often requires physical submission of paper documents to different government agencies which administer trade. Such compliances increase complexity, delay border clearance, and add costs of doing business. 
Some positive initiatives of BBIN countries can be cited also as Bangladesh waived import duties on medical supplies and in many cases accepted e-copies for the assessment of goods (selected importing countries), The adoption of the Faceless Assessment Program by the Indian Customs (pilot testing in the stations of Chennai, Bangalore, Mumbai and Delhi and India has also established a dedicated single window COVID-19 helpdesk. On the other hand, the Department of Customs of Nepal has established a ‘Quick Response Team’ which coordinates all the customs offices, issues relating to customs clearance and concerned stakeholders to facilitate the movement of relief and essential supplies. South Asia’s ‘Land of the Thunder Dragon’, Bhutan has implemented zero contact clearance procedures where customs officials have zero contact with foreign nationals with absolute physical distance maintained and follow disciplined process steps. 
<br>
So, land ports of this region and other parts of the world as well have a long positive impact on international trade. In the below, we like to shed light on the border management and status of land borders of Bangladesh and also focus on the initiatives taken by those countries for the trade facilitation in the region. 
<br>
<b>Land Ports of Bangladesh </b>
<br>

So far, twenty four Land Customs Stations have been declared as Land Ports (Figure: Land Ports of Bangladesh). Of them, twelve land ports are wholly in operation. Among twelve active land ports, seven ports are being operated by Bangladesh Land Port Authority (BLPA) own management and five land ports are being operated by Private Port Operators on BOT (Build, Operate and Transfer) basis. A Private Port Operator has also been appointed to develop and operate Birol Land Port. The remaining twelve land ports are waiting for the development and operation activities. <br>
<br>
<b>Cross-border Paperless Trade </b>
<br>
<img src="https://i.imgur.com/A7jbKKm.png" style="width: 500px;padding: 10px;"> 
<br>
In this pandemic situation, everyone intends to apply all sorts of options to reduce human interactions to halt the spread of COVID 19. The concept of cross- border paperless trade was coined nicely in the Framework Agreement on Facilitation of Cross-border Paperless Trade in Asia and the Pacific, adopted as a UN treaty during 2016. The Framework Agreement is fully dedicated to the digitalization of trade processes, aiming to enable the exchange and mutual recognition of trade-related data and documents in electronic form among national and sub-regional paperless trade systems. It has been found that during the pandemic, COVID 19, the Automation of the system or process (focusing international trade) was leveraged. Bangladesh has taken some visible initiatives since this decade like ‘Customs Modernization Strategic Action Plan 2019-22’; Electronic National Single Windows (NSW) which will help facilitate international trade by allowing traders (importers and exporters) to file all import, export and transit information with the help of a dedicated, single electronic gateway and in turn will reduce the greater probability of face-to-face interactions. 
<br>
<b>Customs Modernization Strategic Action Plan 2019-22 of Bangladesh Customs </b>
<br>
Customs Modernization Strategic Action Plan 2019-22 of Bangladesh Customs envisages a comprehensive development plan for Bangladesh Customs with time-bound, evidence-based and results-driven activities to be implemented in the next four years. This strategic action plan features eleven (11) key areas focusing on strategic planning, enabling regulatory framework, infrastructure development, streamlining ICT application, trade and passenger facilitation, tariff rationalization, modernization of bonded warehouse facilities, HR Governance, regional and international integration and communication. <br>
<br>
<b>National Single Window (NSW) </b>
<br>
The National Single Window (NSW) project has been taken up to connect all stakeholders in a single platform. It will provide the facilities of One Stop Service for import and export. Full implementation of the modern and technology-based NSW project will speed up trade and reduce business operating costs. This will be an important step in the implementation of cross-border paperless trade. 
<br>

<b>Increase the Cooperation among Border Agencies </b>
<br>
It is obvious that borders have tendencies to be strict in allowing the export of goods, during such critical times or in such pandemic states. We understand that trade receives great impact during such times (COVID 19) when trade pauses. So, it is critical to expedite the relationships among all related agencies like national customs, land border authorities and other agencies. <br>
The COVID-19 pandemic presents an important opportunity for countries in the South Asian region to automate and improve critical infrastructure and border management processes. Moreover, the pandemic also paves the way for increasing the relationship among regional trade blocks like SAFTA, BIMSTEC, BCIM, where Bangladesh is a vital member and various WTO related compliances like Sanitary Phytosanitary (SPS) measures, Technical Barriers to Trade (TBT) (all are Non-Tariff Measures) and border management issues play pivotal role in international trade, during the pandemic. 


