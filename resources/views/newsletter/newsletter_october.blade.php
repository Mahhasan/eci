                        Land Ports are also called border stations for import
                        and export. A land port houses the customs and border
                        protection, and other inspection agencies responsible
                        for the enforcement of the country's laws pertaining to
                        such activities. The land port of entry consists of the
                        land, the buildings, and the on-site roadways and
                        parking lots that the port of entry occupies. The
                        facility serves as a point of contact for travelers
                        entering or leaving the country for the purposes of
                        enforcement; Prevention of illegal aliens from entering
                        the country; Collection of revenues; Prevention of
                        injurious plants, animal pests, human and animal
                        diseases from entering the country; Examination of
                        export and import documents; Registration of valuable
                        articles being temporarily taken out of the country; and
                        Commercial transactions. Land ports of Bangladesh are
                        located at land and inland water (river) boundaries with
                        India, Nepal, Bhutan and Myanmar, which have varying
                        needs and requirements based on their location. For
                        example, one land port of entry could be on a major
                        border trade route and, therefore, process a high volume
                        of commercial traffic and yet have virtually
                        insignificant pedestrian traffic. On the other hand,
                        another land port of entry can process as many as 15,000
                        pedestrians each day. Beyond these obvious programmatic
                        differences, the perceived transaction at a land port of
                        entry will have a significant impact on individual land
                        port of entry administration. 
						<br><br>
						Bangladesh Land Port
                        Authority (BLPA) came into being under Bangladesh Sthala
                        Bandar Kartipaksha Act, 2001 (Act 20 of 2001) to
                        facilitate and improve import and export between
                        Bangladesh and neighbouring countries. Since inception,
                        Bangladesh Land Port Authority has been functioning
                        under the Ministry of Shipping. So far 23 Land Customs
                        Stations have been declared as Land Ports. Of the
                        declared land ports, namely Benapole, Bhomra, Burimari,
                        Akhaura, Nakugaon and Tamabil are being operated by own
                        management of BLPA. On the other hand, Sonamosjid, Hili,
                        Teknaf, Bibirbazar and Banglabandha Land Ports are being
                        operated by Private Port Operators on BOT (Build,
                        Operate and Transfer) basis. A Private Port Operator has
                        also been appointed to develop and operate Birol Land
                        Port. The development of the remaining 11 land ports
                        (Darshona, Belonia, Gobrakura-Koroitoli, Ramgarh,
                        Sonahat, Chilahati, Tegamukh, Daulatganj, Sheola, Dhanua
                        Kamalpur, Balla) is under process. The total number of
                        approved manpower for BLPA is 345

